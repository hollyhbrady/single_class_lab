class Team:
    def __init__(self, name, players, coach):
        self.name = name
        self.players = players
        self.coach = coach
        # self.points = points

    def team_has_name(self):
        return name

    def team_has_players(self):
        return players

    def team_has_coach(self):
        return coach

    def add_player(self, name):
        self.players.append(name)

    def has_player(self, name):
        if name == name:
            return True
        
        return False

    
        




